import torch
import torch.nn as nn
from torchvision import models

class HybridResNetViTSmall(nn.Module):
    def __init__(self, num_classes=9):
        super(HybridResNetViTSmall, self).__init__()

        # 1. Front-end: ResNet18 (Feature Extractor)
        resnet = models.resnet18(weights=models.ResNet18_Weights.IMAGENET1K_V1)
        self.backbone = nn.Sequential(*list(resnet.children())[:-2])

        # 2. Bridge: Chuyển từ 512 channels (ResNet) sang 384 channels (ViT-Small)
        self.feature_projection = nn.Conv2d(512, 384, kernel_size=1)

        # 3. Positional Encoding cho 49 patches (7x7) với 384 chiều
        self.pos_embedding = nn.Parameter(torch.randn(1, 49, 384))

        # 4. Back-end: Transformer Encoder chuẩn ViT-Small
        encoder_layer = nn.TransformerEncoderLayer(
            d_model=384,
            nhead=6,
            dim_feedforward=1536, # Thường gấp 4 lần d_model
            dropout=0.1,
            batch_first=True
        )
        self.transformer = nn.TransformerEncoder(encoder_layer, num_layers=8)

        # 5. Head phân loại
        self.classifier = nn.Sequential(
            nn.LayerNorm(384),
            nn.Linear(384, num_classes)
        )

    def forward(self, x, return_attention=False):
        # Bước 1: Trích xuất đặc trưng cục bộ (B, 512, 7, 7)
        features = self.backbone(x)

        # Bước 2: Projection sang không gian của ViT-Small (B, 384, 7, 7)
        features = self.feature_projection(features)

        # Bước 3: Flatten và thêm Position Embedding
        b, c, h, w = features.shape
        x = features.view(b, c, h * w).permute(0, 2, 1) # (B, 49, 384)
        x = x + self.pos_embedding

        # Chế độ lấy Attention để vẽ Heatmap
        if return_attention:
            for i, layer in enumerate(self.transformer.layers):
                if i == len(self.transformer.layers) - 1:
                    x_new, attn_weights = layer.self_attn(x, x, x, average_attn_weights=True)
                    x = layer.norm1(x + layer.dropout1(x_new))
                    x = layer.norm2(x + layer.dropout2(layer.linear2(layer.dropout(torch.nn.functional.relu(layer.linear1(x))))))
                    return self.classifier(x.mean(dim=1)), attn_weights
                x = layer(x)

        # Bước 4: Transformer xử lý Global Context
        x = self.transformer(x)

        # Bước 5: Pooling và Classification
        return self.classifier(x.mean(dim=1))

# Khởi tạo mô hình
model_hybrid = HybridResNetViTSmall(num_classes=9).to(device)

import torch
import torch.nn as nn
from torchvision import models
import torch.nn.functional as F

class PretrainedHybridResNetViT(nn.Module):
    def __init__(self, num_classes=9):
        super().__init__()

        # 1. CNN backbone (ResNet18)
        resnet = models.resnet18(weights=models.ResNet18_Weights.IMAGENET1K_V1)
        self.backbone = nn.Sequential(*list(resnet.children())[:-2])

        # Freeze CNN (giai đoạn đầu)
        for param in self.backbone.parameters():
            param.requires_grad = False

        # 2. Load full ViT pretrained
        self.vit = models.vit_b_16(weights=models.ViT_B_16_Weights.IMAGENET1K_V1)

        # ❗ Bỏ patch embedding vì đã có CNN
        self.vit.conv_proj = nn.Identity()

        # 3. Bridge: 512 → 768
        self.bridge = nn.Conv2d(512, 768, kernel_size=1)

        # 4. Resize positional embedding (giữ pretrained)
        self._resize_pos_embed()

        # 5. Classifier
        self.vit.heads = nn.Linear(768, num_classes)

    def _resize_pos_embed(self):
        pos_embed = self.vit.encoder.pos_embedding  # (1, 197, 768)

        cls_pos = pos_embed[:, :1, :]
        grid_pos = pos_embed[:, 1:, :]

        grid_pos = grid_pos.reshape(1, 14, 14, 768).permute(0, 3, 1, 2)

        grid_pos = F.interpolate(
            grid_pos,
            size=(7, 7),
            mode='bicubic',
            align_corners=False
        )

        grid_pos = grid_pos.permute(0, 2, 3, 1).reshape(1, -1, 768)

        new_pos_embed = torch.cat([cls_pos, grid_pos], dim=1)

        # ❗ Ghi đè giữ pretrained distribution
        self.vit.encoder.pos_embedding.data = new_pos_embed

    def forward(self, x):
        # CNN → (B, 512, 7, 7)
        x = self.backbone(x)

        # Bridge → (B, 768, 7, 7)
        x = self.bridge(x)

        # Flatten → (B, 49, 768)
        B, C, H, W = x.shape
        x = x.flatten(2).transpose(1, 2)

        # CLS token (dùng của ViT)
        cls_token = self.vit.class_token.expand(B, -1, -1)
        x = torch.cat((cls_token, x), dim=1)

        # Encoder chuẩn của ViT
        x = self.vit.encoder(x)

        # Lấy CLS token
        x = x[:, 0]

        return self.vit.heads(x)

