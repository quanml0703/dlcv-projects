import numpy as np
import cv2
import matplotlib.pyplot as plt
import torch
import math

def visualize_hybrid_attention(model, dataset, device, idx=0):
    model.eval()

    # 1. Lấy ảnh
    image, label = dataset[idx]
    input_tensor = image.unsqueeze(0).to(device)

    # 2. Forward
    with torch.no_grad():
        logits, attn = model(input_tensor, return_attention=True)
        pred = torch.argmax(logits, dim=1).item()

    # 3. Xử lý attention
    if len(attn.shape) == 4:
        attn = attn.mean(dim=1)  # average heads

    attn = attn[0]  # [tokens, tokens]

    # CLS attention
    importance = attn[0, 1:]  # bỏ CLS
    importance = importance.cpu().numpy()

    # 🔥 FIX QUAN TRỌNG: tự động reshape
    num_patches = importance.shape[0]
    size = int(math.sqrt(num_patches))

    if size * size == num_patches:
        mask = importance.reshape(size, size)
    else:
        # fallback nếu không phải hình vuông
        h = size
        w = num_patches // size
        mask = importance[:h*w].reshape(h, w)

    print(f"Num patches: {num_patches}, reshape: {mask.shape}")

    # 4. Resize heatmap
    h_img, w_img = image.shape[1], image.shape[2]
    heatmap = cv2.resize(mask, (w_img, h_img))
    heatmap = (heatmap - heatmap.min()) / (heatmap.max() - heatmap.min() + 1e-8)

    # 5. De-normalize
    img = image.permute(1, 2, 0).cpu().numpy()
    mean = np.array([0.485, 0.456, 0.406])
    std = np.array([0.229, 0.224, 0.225])
    img = std * img + mean
    img = np.clip(img, 0, 1)

    # 6. Overlay
    heatmap_uint8 = np.uint8(255 * heatmap)
    heatmap_color = cv2.applyColorMap(heatmap_uint8, cv2.COLORMAP_JET)
    heatmap_color = cv2.cvtColor(heatmap_color, cv2.COLOR_BGR2RGB) / 255.0
    overlay = cv2.addWeighted(img, 0.6, heatmap_color, 0.4, 0)

    # 7. Plot
    plt.figure(figsize=(15, 5))
    titles = [f"Original (Label: {label})", "Attention Heatmap", f"Prediction: {pred}"]
    imgs = [img, heatmap, overlay]

    for i in range(3):
        plt.subplot(1, 3, i+1)
        plt.imshow(imgs[i], cmap='jet' if i == 1 else None)
        plt.title(titles[i])
        plt.axis("off")

    plt.show()

