def show_vit_attention_rollout(model, dataset, device, idx=0):
    model.eval()

    qkv_list = []

    # hook qkv (ổn định nhất)
    def hook_fn(module, input, output):
        qkv_list.append(output)

    hooks = []
    for blk in model.blocks:
        h = blk.attn.qkv.register_forward_hook(hook_fn)
        hooks.append(h)

    # lấy ảnh
    image, label = dataset[idx]
    input_tensor = image.unsqueeze(0).to(device)

    # forward
    with torch.no_grad():
        logits = model(input_tensor)
        pred = torch.argmax(logits, dim=1).item()

    # remove hooks
    for h in hooks:
        h.remove()

    # ===== TÍNH ATTENTION TỪ QKV =====
    attn_all = []

    for qkv in qkv_list:
        B, N, C = qkv.shape
        num_heads = model.blocks[-1].attn.num_heads
        head_dim = C // (3 * num_heads)

        qkv = qkv.reshape(B, N, 3, num_heads, head_dim)
        qkv = qkv.permute(2, 0, 3, 1, 4)

        q, k, v = qkv[0], qkv[1], qkv[2]

        attn = (q @ k.transpose(-2, -1)) * (1.0 / np.sqrt(head_dim))
        attn = torch.softmax(attn, dim=-1)

        attn_all.append(attn)

    # stack
    attn = torch.stack(attn_all)      # [L, B, heads, N, N]
    attn = attn.mean(dim=2)           # avg heads

    # ===== ROLLOUT =====
    N = attn.size(-1)
    eye = torch.eye(N).to(attn.device)
    attn = attn + eye
    attn = attn / attn.sum(dim=-1, keepdim=True)

    joint_attn = attn[0]
    for i in range(1, attn.size(0)):
        joint_attn = attn[i] @ joint_attn

    # CLS → patches
    mask = joint_attn[0, 0, 1:].cpu().numpy()

    size = int(np.sqrt(mask.shape[0]))
    mask = mask.reshape(size, size)

    # ===== HEATMAP =====
    h_img, w_img = image.shape[1], image.shape[2]
    heatmap = cv2.resize(mask, (w_img, h_img))
    heatmap = heatmap / (heatmap.max() + 1e-8)

    heatmap_uint8 = np.uint8(255 * heatmap)
    heatmap_color = cv2.applyColorMap(heatmap_uint8, cv2.COLORMAP_JET)

    # ===== IMAGE =====
    img = image.permute(1, 2, 0).cpu().numpy()
    img = (img - img.min()) / (img.max() - img.min() + 1e-8)

    overlay = 0.6 * img + 0.4 * (heatmap_color / 255.0)

    # ===== SHOW =====
    plt.figure(figsize=(12,4))

    plt.subplot(1,3,1)
    plt.imshow(img)
    plt.title(f"Original (Label: {int(label)})")
    plt.axis("off")

    plt.subplot(1,3,2)
    plt.imshow(heatmap, cmap='jet')
    plt.title("Attention Rollout")
    plt.axis("off")

    plt.subplot(1,3,3)
    plt.imshow(overlay)
    plt.title(f"Pred: {pred}")
    plt.axis("off")

    plt.show()

