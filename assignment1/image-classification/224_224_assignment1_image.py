def visualize_rollout_final(model, dataset, device, idx=0):
    model.eval()
    qkv_list = []

    # 1. Hook để lấy dữ liệu QKV (giữ nguyên)
    def hook_fn(module, input, output):
        qkv_list.append(output.detach().cpu())

    hooks = []
    for blk in model.blocks:
        h = blk.attn.qkv.register_forward_hook(hook_fn)
        hooks.append(h)

    # 2. Lấy dữ liệu mẫu (giữ nguyên)
    image, label = dataset[idx]
    input_tensor = image.unsqueeze(0).to(device)

    # 3. Forward pass (giữ nguyên)
    with torch.no_grad():
        logits = model(input_tensor)
        pred = torch.argmax(logits, dim=1).item()

    # Gỡ hooks ngay lập tức (giữ nguyên)
    for h in hooks:
        h.remove()

    # 4. Tính toán Rollout chuẩn (giữ nguyên)
    num_layers = len(qkv_list)
    num_heads = model.blocks[0].attn.num_heads
    N = qkv_list[0].shape[1]
    eye = torch.eye(N)
    joint_attn = torch.eye(N)

    for i in range(num_layers):
        qkv = qkv_list[i]
        B, N, C3 = qkv.shape
        head_dim = C3 // (3 * num_heads)
        qkv_reshaped = qkv.reshape(B, N, 3, num_heads, head_dim).permute(2, 0, 3, 1, 4)
        q, k = qkv_reshaped[0], qkv_reshaped[1]
        attn = (q @ k.transpose(-2, -1)) * (head_dim ** -0.5)
        attn = torch.softmax(attn, dim=-1)
        layer_attn = attn[0].mean(dim=0)
        aug_attn = (layer_attn + eye) / 2
        joint_attn = aug_attn @ joint_attn

    # 5. Trích xuất Mask (giữ nguyên)
    mask = joint_attn[0, 1:].numpy()
    grid_size = int(np.sqrt(mask.shape[0]))
    mask = mask.reshape(grid_size, grid_size)

    # 6. Heatmap & Overlay (PHẦN SỬA ĐỔI CHÍNH TẠI ĐÂY)

    # Resize mask về kích thước ảnh gốc
    mask_resized = cv2.resize(mask, (image.shape[2], image.shape[1]))
    # Chuẩn hóa mask về khoảng [0, 1]
    mask_norm = (mask_resized - mask_resized.min()) / (mask_resized.max() - mask_resized.min() + 1e-8)

    # Tạo bản đồ nhiệt màu (Jet)
    heatmap_uint8 = np.uint8(255 * mask_norm)
    heatmap_color = cv2.applyColorMap(heatmap_uint8, cv2.COLORMAP_JET)
    heatmap_color = cv2.cvtColor(heatmap_color, cv2.COLOR_BGR2RGB) / 255.0 # Chuyển sang RGB chuẩn [0, 1]

    # Chuẩn hóa ảnh gốc về khoảng [0, 1]
    img_np = image.permute(1, 2, 0).cpu().numpy()
    img_np = (img_np - img_np.min()) / (img_np.max() - img_np.min() + 1e-8)

    # --- LOGIC TRỘN MÀU MỚI (MULTIPLY + BLEND) ---
    # Phép nhân giúp vùng attention thấp bị tối đi
    # Phép cộng giúp giữ lại một chút chi tiết ảnh gốc ở vùng attention cao

